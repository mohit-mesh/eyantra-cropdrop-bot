int main(void)
{
  HAL_Init();
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();

  /* USER CODE BEGIN 2 */
  // Start the Motor PWM Timers
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);
  /* USER CODE END 2 */

  /* Infinite loop */
  while (1)
  {
    /* USER CODE BEGIN 3 */
    // 1. Read Waveshare Sensors (Logic 1 = White Line, 0 = Black Floor)
    uint8_t ir1 = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_0); // Far Left
    uint8_t ir2 = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_1); // Left
    uint8_t ir3 = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_2); // Center
    uint8_t ir4 = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_0); // Right
    uint8_t ir5 = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_1); // Far Right

    // 2. Map Sensors to Error Value
    if (ir3 == 1)      error = 0;   // Perfectly centered
    else if (ir2 == 1) error = -1;  // Slightly left
    else if (ir4 == 1) error = 1;   // Slightly right
    else if (ir1 == 1) error = -2;  // Hard left
    else if (ir5 == 1) error = 2;   // Hard right
    else {
        // Line lost - use memory to continue turn
        if (last_error < 0) error = -3;
        else if (last_error > 0) error = 3;
    }

    // 3. PID Math Calculation
    integral += error;
    if (integral > 255)  integral = 255;  // Anti-windup
    if (integral < -255) integral = -255;

    derivative = error - last_error;
    turn_correction = (Kp * error) + (Ki * integral) + (Kd * derivative);
    last_error = error;

    // 4. Calculate Final Motor Speeds
    int left_out  = base_speed + (int)turn_correction;
    int right_out = base_speed - (int)turn_correction;

    // 5. Apply to Motors
    Set_Motor_Speed(left_out, right_out);

    // 6. Loop Delay (100Hz frequency)
    HAL_Delay(10);
    /* USER CODE END 3 */
  }
}

