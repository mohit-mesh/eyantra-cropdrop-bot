/**
  * @brief  Motor control function for N20 motors.
  * @param  leftSpeed: PWM value for left motor (-255 to 255)
  * @param  rightSpeed: PWM value for right motor (-255 to 255)
  */
void Set_Motor_Speed(int leftSpeed, int rightSpeed) {
    // Constraints to prevent PWM overflow (Your ARR is 255)
    if (leftSpeed > 255)  leftSpeed = 255;
    if (rightSpeed > 255) rightSpeed = 255;
    if (leftSpeed < -255) leftSpeed = -255;
    if (rightSpeed < -255) rightSpeed = -255;

    // --- LEFT MOTOR (PA0/PA1) ---
    if (leftSpeed >= 0) {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, leftSpeed);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
    } else {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, -leftSpeed);
    }

    // --- RIGHT MOTOR (PB10/PB11) ---
    if (rightSpeed >= 0) {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, rightSpeed);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, 0);
    } else {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, 0);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, -rightSpeed);
    }
}
